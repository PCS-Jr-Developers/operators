# Equal (==)
print(5 == 5)
# Output: True

# Not Equal (!=)
print(5 != 3)
# Output: True

# Greater Than (>)
print(10 > 7)
# Output: True

# Less Than (<)
print(3 < 8)
# Output: True

# Greater Than or Equal To (>=)
print(5 >= 5)
# Output: True

# Less Than or Equal To (<=)
print(12 <= 15)
# Output: True
