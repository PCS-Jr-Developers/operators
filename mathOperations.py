# Addition (+)
result_add = 3 + 5
print("3 + 5 =", result_add)

# Subtraction (-)
result_subtract = 10 - 7
print("10 - 7 =", result_subtract)

# Multiplication (*)
result_multiply = 4 * 6
print("4 * 6 =", result_multiply)

# Division (/)
result_divide = 15 / 3
print("15 / 3 =", result_divide)

# Integer Division (//)
result_int_divide = 15 // 3
print("15 // 3 =", result_int_divide)

# Modulus (%)
result_modulus = 17 % 5
print("17 % 5 =", result_modulus)

# Exponentiation (**)
result_exponent = 2 ** 3
print("2 ** 3 =", result_exponent)
